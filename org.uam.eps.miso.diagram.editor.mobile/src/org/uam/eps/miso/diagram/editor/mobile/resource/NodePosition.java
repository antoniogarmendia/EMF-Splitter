package org.uam.eps.miso.diagram.editor.mobile.resource;

public class NodePosition {
	
	private int x;
	private int y;
	
	public NodePosition(int x, int y) {
		super();
		this.x = x;
		this.y = y;
	}

	public int getX() {
		return x;
	}

	public void setX(int x) {
		this.x = x;
	}

	public int getY() {
		return y;
	}

	public void setY(int y) {
		this.y = y;
	}

}
